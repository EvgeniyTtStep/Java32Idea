package org.Kondrashov.HomeworkJAVA_36;

public class Glasses {

    Frame frame;

    Lens lens;

    public Glasses() {
        frame = null;
        lens = null;
    }



    public Frame getFrame() {
        return frame;
    }

    public void setFrame(Frame pFrame) {
        frame = pFrame;
    }


    public Lens getLens() {
        return lens;
    }

    public void setLens(Lens pLens) {
        lens = pLens;
    }


}
